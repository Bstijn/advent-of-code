            foreach (var movement in movements)
            {
                switch (movement.Type)
                {
                    case MovementType.Down:
                        //depth += movement.Amount;
                        aim += movement.Amount;
                        break;
                    case MovementType.Up:
                        //depth -= movement.Amount;
                        aim -=movement.Amount; 
                        break;
